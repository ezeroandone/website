/// Hire an applicant: atomically promote their application to `Hired`, create
/// a `staff` row, create a `staff_lifecycle` row with `Probation` status, and
/// dispatch an onboarding email.
///
/// Expects JSON body `{ probation_months: u8 }` where the value must be
/// in the range 3–6 inclusive.
///
/// Returns HTTP 201 `{ "staff_id": "..." }` on success.
/// Returns HTTP 400 if `probation_months` is out of range.
/// Returns HTTP 404 if the application does not exist.
/// Returns HTTP 400 if the application is not in `Offered` status.
/// Returns HTTP 500 (with best-effort compensating deletes) on any DB failure.
///
/// Requires Admin role (enforced by router).
///
/// Requirements: 7.4, 7.5, 7.6, 7.7, 7.8
pub async fn hire_applicant(
    mut req: Request,
    env: &Env,
    _ctx: SessionContext,
    application_id: &str,
) -> Result<Response> {
    // ── Parse and validate body ──────────────────────────────────────────────
    let body: serde_json::Value = req
        .json()
        .await
        .unwrap_or(serde_json::Value::Object(serde_json::Map::new()));

    let probation_months = match body.get("probation_months").and_then(|v| v.as_u64()) {
        Some(n) => n,
        None => {
            return error_to_response(WorkerError::Validation(ValidationError::InvalidInput(
                "probation_months is required and must be a positive integer".into(),
            )));
        }
    };

    // Req 7.4, 7.5 — valid range is [3, 6] inclusive
    if !(3..=6).contains(&probation_months) {
        return error_to_response(WorkerError::Validation(ValidationError::InvalidInput(
            "probation_months must be between 3 and 6 inclusive".into(),
        )));
    }

    let db = env
        .d1("DB")
        .map_err(|e| WorkerError::Internal(e.to_string()))?;

    // ── Load the application ─────────────────────────────────────────────────
    let application = db
        .prepare(
            "SELECT id, career_id, applicant_name, applicant_email, cover_letter, \
             status, applied_at, updated_at \
             FROM application WHERE id = ?1",
        )
        .bind(&[application_id.into()])
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .first::<Application>(None)
        .await
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .ok_or(WorkerError::NotFound)?;

    // Application must be in `Offered` status to be hired
    if application.status != ApplicationStatus::Offered {
        return error_to_response(WorkerError::Validation(ValidationError::InvalidTransition));
    }

    let applicant_email = application.applicant_email.clone();

    // Derive username from the email local-part (everything before @).
    // D1's DEFAULT generates the id; we provide the username here.
    let username = applicant_email
        .split('@')
        .next()
        .unwrap_or("staff")
        .to_lowercase();

    // ── Step 1: Update application status to 'Hired' ─────────────────────────
    db.prepare(
        "UPDATE application SET status = 'Hired', updated_at = unixepoch() WHERE id = ?1",
    )
    .bind(&[application_id.into()])
    .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
    .run()
    .await
    .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?;

    // ── Step 2: Insert staff row ─────────────────────────────────────────────
    // id is generated by the D1 DEFAULT (lower(hex(randomblob(16)))).
    // We insert without an explicit id and then query back by email.
    let insert_staff_result = db
        .prepare(
            "INSERT INTO staff (email, username, role, onboarding_completed) \
             VALUES (?1, ?2, 'Staff', 0)",
        )
        .bind(&[applicant_email.as_str().into(), username.as_str().into()])
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .run()
        .await;

    if let Err(e) = insert_staff_result {
        // Compensate: revert application status back to 'Offered'
        let _ = db
            .prepare(
                "UPDATE application SET status = 'Offered', updated_at = unixepoch() \
                 WHERE id = ?1",
            )
            .bind(&[application_id.into()])
            .map_err(|_| ())
            .and_then(|stmt| {
                // best-effort — ignore if this also fails
                drop(stmt);
                Ok(())
            });
        return error_to_response(WorkerError::Db(DbError::Query(e.to_string())));
    }

    // Retrieve the generated staff_id by looking up the email
    let staff_id_row = db
        .prepare("SELECT id FROM staff WHERE email = ?1")
        .bind(&[applicant_email.as_str().into()])
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .first::<serde_json::Value>(None)
        .await
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?;

    let staff_id = match staff_id_row.and_then(|row| {
        row.get("id")
            .and_then(|v| v.as_str())
            .map(|s| s.to_string())
    }) {
        Some(id) => id,
        None => {
            // Compensate: revert application
            let _ = db
                .prepare(
                    "UPDATE application SET status = 'Offered', updated_at = unixepoch() \
                     WHERE id = ?1",
                )
                .bind(&[application_id.into()])
                .ok();
            return error_to_response(WorkerError::Internal(
                "Failed to retrieve generated staff_id after insert".into(),
            ));
        }
    };

    // ── Step 3: Insert staff_lifecycle row ───────────────────────────────────
    // probation_end = unixepoch() + probation_months * 30 * 86400
    let probation_seconds: i64 = probation_months as i64 * 30 * 86_400;

    let insert_lifecycle_result = db
        .prepare(
            "INSERT INTO staff_lifecycle (staff_id, status, probation_start, probation_end) \
             VALUES (?1, 'Probation', unixepoch(), unixepoch() + ?2)",
        )
        .bind(&[staff_id.as_str().into(), probation_seconds.into()])
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .run()
        .await;

    if let Err(e) = insert_lifecycle_result {
        // Compensate: delete staff row and revert application
        let _ = db
            .prepare("DELETE FROM staff WHERE id = ?1")
            .bind(&[staff_id.as_str().into()])
            .ok();
        let _ = db
            .prepare(
                "UPDATE application SET status = 'Offered', updated_at = unixepoch() \
                 WHERE id = ?1",
            )
            .bind(&[application_id.into()])
            .ok();
        return error_to_response(WorkerError::Db(DbError::Query(e.to_string())));
    }

    // ── Step 4: Dispatch onboarding email ────────────────────────────────────
    // Email failure does not roll back the DB writes — the hire is complete;
    // the email can be retried by the admin if needed.
    if let Err(e) = send_onboarding_email(&applicant_email, &staff_id, env).await {
        // Log but do not surface to caller; hire transaction succeeded.
        console_warn!("hire_applicant: onboarding email failed for staff {}: {:?}", staff_id, e);
    }

    // ── Return HTTP 201 with staff_id ─────────────────────────────────────────
    Response::from_json(&serde_json::json!({ "staff_id": staff_id }))
        .map(|r| r.with_status(201))
        .map_err(|e| WorkerError::Internal(e.to_string()).into())
}

