/// Parse a multipart form, read the `file` field, detect MIME from magic bytes,
/// validate MIME is in `allowed_mimes`, and validate `size <= max_size`.
///
/// Returns `(bytes, original_filename, detected_mime)` on success, or an
/// HTTP 400 `Result<Response>` on failure.
async fn parse_and_validate_image_upload(
    req: &mut Request,
    allowed_mimes: &[&str],
    max_size: usize,
) -> std::result::Result<(Vec<u8>, String, &'static str), Result<Response>> {
    // Parse multipart form
    let form = req
        .form_data()
        .await
        .map_err(|e| {
            Response::from_json(&serde_json::json!({ "error": format!("Failed to parse form data: {}", e) }))
                .map(|r| r.with_status(400))
                .map_err(|e2| WorkerError::Internal(e2.to_string()).into())
        })?;

    // Extract `file` field
    let (file_bytes, original_filename) = match form.get("file") {
        Some(FormEntry::File(file)) => {
            let name = file.name();
            let bytes = file.bytes().await.map_err(|e| {
                Response::from_json(&serde_json::json!({ "error": format!("Failed to read file: {}", e) }))
                    .map(|r| r.with_status(400))
                    .map_err(|e2| WorkerError::Internal(e2.to_string()).into())
            })?;
            (bytes, name)
        }
        _ => {
            return Err(
                Response::from_json(&serde_json::json!({ "error": "file field is required" }))
                    .map(|r| r.with_status(400))
                    .map_err(|e| WorkerError::Internal(e.to_string()).into()),
            );
        }
    };

    // Detect MIME from magic bytes (Req 12.2)
    let mime = match detect_mime_from_magic_bytes(&file_bytes) {
        Some(m) if allowed_mimes.contains(&m) => m,
        _ => {
            return Err(
                Response::from_json(&serde_json::json!({ "error": "Invalid image type" }))
                    .map(|r| r.with_status(400))
                    .map_err(|e| WorkerError::Internal(e.to_string()).into()),
            );
        }
    };

    // Validate file size (Req 12.4, 12.5)
    if file_bytes.len() > max_size {
        return Err(
            Response::from_json(&serde_json::json!({ "error": "File too large" }))
                .map(|r| r.with_status(400))
                .map_err(|e| WorkerError::Internal(e.to_string()).into()),
        );
    }

    Ok((file_bytes, original_filename, mime))
}

