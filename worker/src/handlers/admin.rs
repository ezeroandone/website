/// Transition an application to a new status.
///
/// Request body: `{ "status": "<TargetStatus>" }`
///
/// Behaviour:
/// - Returns HTTP 404 if the application does not exist.
/// - Returns HTTP 400 if the requested transition is not in the allowed set.
/// - On success, updates `status` and `updated_at`, returns HTTP 200.
///
/// Requires Admin role (enforced by router).
///
/// Requirements: 7.1, 7.2, 7.3
pub async fn patch_application_status(
    mut req: Request,
    env: &Env,
    _ctx: SessionContext,
    id: &str,
) -> Result<Response> {
    // ── Parse body ───────────────────────────────────────────────────────────
    let body: serde_json::Value = req
        .json()
        .await
        .unwrap_or(serde_json::Value::Object(serde_json::Map::new()));

    let target_status_str = match body.get("status").and_then(|v| v.as_str()) {
        Some(s) => s.to_string(),
        None => {
            return error_to_response(WorkerError::Validation(ValidationError::InvalidInput(
                "status field is required".into(),
            )));
        }
    };

    // Deserialise the target status from the string value
    let target_status: ApplicationStatus =
        match serde_json::from_value(serde_json::Value::String(target_status_str.clone())) {
            Ok(s) => s,
            Err(_) => {
                return error_to_response(WorkerError::Validation(ValidationError::InvalidInput(
                    format!("Unknown status: {}", target_status_str),
                )));
            }
        };

    let db = env
        .d1("DB")
        .map_err(|e| WorkerError::Internal(e.to_string()))?;

    // ── Load current application status from D1 ───────────────────────────
    // We only need the `status` field here; query only what we need.
    let row = db
        .prepare("SELECT id, career_id, applicant_name, applicant_email, cover_letter, status, applied_at, updated_at FROM application WHERE id = ?1")
        .bind(&[id.into()])
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .first::<Application>(None)
        .await
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?;

    let application = match row {
        Some(a) => a,
        None => return error_to_response(WorkerError::NotFound),
    };

    let current_status = application.status;

    // ── Validate the transition (Req 7.1, 7.2) ───────────────────────────────
    if !is_allowed_transition(current_status, target_status) {
        let msg = format!(
            "Invalid transition from {} to {}",
            status_to_str(current_status),
            status_to_str(target_status)
        );
        return Response::from_json(&serde_json::json!({ "error": msg }))
            .map(|r| r.with_status(400))
            .map_err(|e| e.into());
    }

    // ── Execute the status update (Req 7.3) ──────────────────────────────────
    db.prepare(
        "UPDATE application SET status = ?1, updated_at = unixepoch() WHERE id = ?2",
    )
    .bind(&[status_to_str(target_status).into(), id.into()])
    .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
    .run()
    .await
    .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?;

    Response::from_json(&serde_json::json!({ "id": id, "status": status_to_str(target_status) }))
        .map(|r| r.with_status(200))
        .map_err(|e| WorkerError::Internal(e.to_string()).into())
}

