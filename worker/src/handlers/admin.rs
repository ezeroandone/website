/// Update an existing content post.
///
/// Accepts any subset of `{ title, summary, body_md, published, published_at, slug }`.
/// If `slug` is updated, checks uniqueness within the same `type`
/// (excluding the current post id); returns HTTP 409 on conflict.
/// Always refreshes `updated_at = unixepoch()`.
/// Returns HTTP 200 with the updated post JSON.
///
/// Requires Admin role (enforced by router).
///
/// Requirements: 10.6, 10.8
pub async fn patch_content(mut req: Request, env: &Env, _ctx: SessionContext, id: &str) -> Result<Response> {
    let body: serde_json::Value = req
        .json()
        .await
        .unwrap_or(serde_json::Value::Object(serde_json::Map::new()));

    let db = env
        .d1("DB")
        .map_err(|e| WorkerError::Internal(e.to_string()))?;

    // ── Load current post (need type for slug uniqueness check) ──────────────
    let current = db
        .prepare(
            "SELECT id, type, slug, title, summary, body_md, author_id, \
             published_at, updated_at, published, \
             featured_image_url, category, tags, project_type, technologies, material_icon \
             FROM post WHERE id = ?1",
        )
        .bind(&[id.into()])
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .first::<serde_json::Value>(None)
        .await
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .ok_or(WorkerError::NotFound)?;

    let post_type = current
        .get("type")
        .and_then(|v| v.as_str())
        .unwrap_or("")
        .to_string();

    // ── Validate slug uniqueness if slug is being updated (Req 10.8) ─────────
    if let Some(new_slug) = body.get("slug").and_then(|v| v.as_str()) {
        let conflict = db
            .prepare(
                "SELECT id FROM post WHERE slug = ?1 AND type = ?2 AND id != ?3 LIMIT 1",
            )
            .bind(&[new_slug.into(), post_type.as_str().into(), id.into()])
            .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
            .first::<serde_json::Value>(None)
            .await
            .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?;

        if conflict.is_some() {
            return Response::from_json(&serde_json::json!({ "error": "Slug already in use for this post type" }))
                .map(|r| r.with_status(409))
                .map_err(|e| e.into());
        }
    }

    // ── Build dynamic SET clause ─────────────────────────────────────────────
    let mut set_clauses: Vec<String> = Vec::new();
    let mut bindings: Vec<wasm_bindgen::JsValue> = Vec::new();
    let mut param_idx: u32 = 1;

    macro_rules! push_str_field {
        ($json_key:expr, $col:expr) => {
            if let Some(val) = body.get($json_key).and_then(|v| v.as_str()) {
                set_clauses.push(format!("{} = ?{}", $col, param_idx));
                bindings.push(val.to_string().into());
                param_idx += 1;
            }
        };
    }

    push_str_field!("title",    "title");
    push_str_field!("summary",  "summary");
    push_str_field!("body_md",  "body_md");
    push_str_field!("slug",     "slug");
    push_str_field!("featured_image_url", "featured_image_url");
    push_str_field!("category",           "category");
    push_str_field!("tags",               "tags");
    push_str_field!("project_type",       "project_type");
    push_str_field!("technologies",       "technologies");
    push_str_field!("material_icon",      "material_icon");

    // published is bool → stored as integer
    if let Some(published) = body.get("published").and_then(|v| v.as_bool()) {
        let val: i64 = if published { 1 } else { 0 };
        set_clauses.push(format!("published = ?{}", param_idx));
        bindings.push(val.into());
        param_idx += 1;
    }

    // published_at is an optional integer
    if let Some(published_at) = body.get("published_at").and_then(|v| v.as_i64()) {
        set_clauses.push(format!("published_at = ?{}", param_idx));
        bindings.push(published_at.into());
        param_idx += 1;
    }

    if set_clauses.is_empty() {
        return error_to_response(WorkerError::Validation(ValidationError::InvalidInput(
            "No updatable fields provided".into(),
        )));
    }

    // Always refresh updated_at (Req 10.6)
    set_clauses.push("updated_at = unixepoch()".to_string());

    let sql = format!(
        "UPDATE post SET {} WHERE id = ?{}",
        set_clauses.join(", "),
        param_idx
    );
    bindings.push(id.to_string().into());

    db.prepare(&sql)
        .bind(&bindings)
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .run()
        .await
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?;

    // ── Re-query and return updated row ──────────────────────────────────────
    let post = db
        .prepare(
            "SELECT id, type, slug, title, summary, body_md, author_id, \
             published_at, updated_at, published, \
             featured_image_url, category, tags, project_type, technologies, material_icon \
             FROM post WHERE id = ?1",
        )
        .bind(&[id.into()])
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .first::<serde_json::Value>(None)
        .await
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .ok_or(WorkerError::NotFound)?;

    Response::from_json(&post)
        .map(|r| r.with_status(200))
        .map_err(|e| WorkerError::Internal(e.to_string()).into())
}

