// ---------------------------------------------------------------------------
// GET /api/admin/applications/:id/documents/:doc_id/url
// ---------------------------------------------------------------------------

/// Generate a time-limited presigned URL for a specific application document.
///
/// 1. Queries D1 to fetch `r2_key` for the given `doc_id` + `application_id`.
/// 2. Returns HTTP 404 if not found.
/// 3. Validates `r2_key.starts_with("applications/")` → HTTP 403 if not.
/// 4. Calls `generate_presigned_url` with TTL 300 seconds.
/// 5. Returns `{ "url": "<presigned>", "expires_at": <unix_timestamp> }`.
///
/// Requires Admin role (enforced by router).
///
/// Requirements: 12.9, 12.10, 12.11
pub async fn handle_get_document_presigned_url(
    application_id: &str,
    doc_id: &str,
    _ctx: SessionContext,
    env: &Env,
) -> Result<Response> {
    let db = env
        .d1("DB")
        .map_err(|e| WorkerError::Internal(e.to_string()))?;

    // Query the r2_key for this specific document belonging to this application
    let row = db
        .prepare(
            "SELECT r2_key FROM application_document \
             WHERE id = ?1 AND application_id = ?2",
        )
        .bind(&[doc_id.into(), application_id.into()])
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .first::<serde_json::Value>(None)
        .await
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?;

    let r2_key = match row.and_then(|v| {
        v.get("r2_key")
            .and_then(|k| k.as_str())
            .map(|s| s.to_string())
    }) {
        Some(k) => k,
        None => return error_to_response(WorkerError::NotFound),
    };

    // Security: only allow keys under applications/ (Req 12.11)
    if !r2_key.starts_with("applications/") {
        return error_to_response(WorkerError::Forbidden);
    }

    let bucket = env
        .bucket("EZO_MEDIA")
        .map_err(|e| WorkerError::Internal(format!("Failed to get R2 bucket: {}", e)))?;

    // Generate presigned URL with 300-second TTL
    let presigned_url = generate_presigned_url(&bucket, &r2_key, 300)
        .await
        .map_err(|e| WorkerError::Internal(format!("Presigned URL generation failed: {:?}", e)))?;

    // Compute expiry: current unix time + 300 seconds
    let expires_at = now_unix() + 300;

    Response::from_json(&serde_json::json!({
        "url": presigned_url,
        "expires_at": expires_at,
    }))
    .map(|r| r.with_status(200))
    .map_err(|e| WorkerError::Internal(e.to_string()).into())
}

// ---------------------------------------------------------------------------
// now_unix helper
// ---------------------------------------------------------------------------

/// Return the current Unix timestamp in seconds.
///
/// Uses the Workers `Date` API available from `worker::Date`.
fn now_unix() -> i64 {
    // Date::now().as_millis() returns u64 milliseconds since epoch
    (Date::now().as_millis() / 1000) as i64
}

