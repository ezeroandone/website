/// Generate a URL-safe slug from a title string.
///
/// Transformation: lowercase, spaces → `-`, strip non-alphanumeric except `-`.
fn generate_slug(title: &str) -> String {
    title
        .to_lowercase()
        .chars()
        .map(|c| if c.is_ascii_alphanumeric() || c == '-' { c } else if c == ' ' { '-' } else { '\0' })
        .filter(|&c| c != '\0')
        .collect::<String>()
        // Collapse consecutive hyphens
        .split('-')
        .filter(|s| !s.is_empty())
        .collect::<Vec<&str>>()
        .join("-")
}

/// Find the first available slug for a given `post_type`, starting from
/// `base_slug` and appending `-2`, `-3`, … until no collision is found.
///
/// If `exclude_id` is provided the slug check ignores that post row (used
/// during PATCH to allow keeping the same slug).
///
/// Requirements: 10.5, 10.8
async fn find_unique_slug(
    db: &D1Database,
    base_slug: &str,
    post_type: &str,
    exclude_id: Option<&str>,
) -> StdResult<String, WorkerError> {
    let mut candidate = base_slug.to_string();
    let mut counter: u32 = 1;

    loop {
        let conflict = match exclude_id {
            Some(eid) => {
                db.prepare(
                    "SELECT id FROM post WHERE slug = ?1 AND type = ?2 AND id != ?3 LIMIT 1",
                )
                .bind(&[candidate.as_str().into(), post_type.into(), eid.into()])
                .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
                .first::<serde_json::Value>(None)
                .await
                .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
            }
            None => {
                db.prepare(
                    "SELECT id FROM post WHERE slug = ?1 AND type = ?2 LIMIT 1",
                )
                .bind(&[candidate.as_str().into(), post_type.into()])
                .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
                .first::<serde_json::Value>(None)
                .await
                .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
            }
        };

        if conflict.is_none() {
            return Ok(candidate);
        }

        counter += 1;
        candidate = format!("{}-{}", base_slug, counter);

        // Safety guard: prevent infinite loops if something goes very wrong.
        if counter > 1000 {
            return Err(WorkerError::Internal(
                "Could not find unique slug after 1000 attempts".into(),
            ));
        }    }
}

