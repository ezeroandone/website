/// Create a new career listing.
///
/// Expects JSON body `{ slug, title, description_md?, department?, type, active? }`.
/// Validates `type` ∈ `{Full-Time, Part-Time, Contract, Internship}`.
/// Returns HTTP 201 with the created resource.
///
/// Requires Admin role (enforced by router).
///
/// Requirements: 5.2, 5.5
pub async fn create_career(
    mut req: Request,
    env: &Env,
    _ctx: SessionContext,
) -> Result<Response> {
    // ── Parse body ───────────────────────────────────────────────────────────
    let body: serde_json::Value = req
        .json()
        .await
        .unwrap_or(serde_json::Value::Object(serde_json::Map::new()));

    let slug = body
        .get("slug")
        .and_then(|v| v.as_str())
        .unwrap_or("")
        .trim()
        .to_string();

    let title = body
        .get("title")
        .and_then(|v| v.as_str())
        .unwrap_or("")
        .trim()
        .to_string();

    let description_md = body
        .get("description_md")
        .and_then(|v| v.as_str())
        .unwrap_or("")
        .to_string();

    let department = body
        .get("department")
        .and_then(|v| v.as_str())
        .unwrap_or("")
        .to_string();

    let career_type = body
        .get("type")
        .and_then(|v| v.as_str())
        .unwrap_or("")
        .to_string();

    // active defaults to true when not supplied
    let active = body
        .get("active")
        .and_then(|v| v.as_bool())
        .unwrap_or(true);

    // ── Validate required fields ─────────────────────────────────────────────
    if slug.is_empty() || title.is_empty() {
        return error_to_response(WorkerError::Validation(
            ValidationError::InvalidInput("slug and title are required".into()),
        ));
    }

    // ── Validate type (Req 5.2) ──────────────────────────────────────────────
    if !ALLOWED_CAREER_TYPES.contains(&career_type.as_str()) {
        return error_to_response(WorkerError::Validation(
            ValidationError::InvalidInput(
                "type must be one of: Full-Time, Part-Time, Contract, Internship".into(),
            ),
        ));
    }

    let db = env
        .d1("DB")
        .map_err(|e| WorkerError::Internal(e.to_string()))?;

    // ── Insert the new career row ─────────────────────────────────────────────
    // id is auto-generated by DEFAULT (lower(hex(randomblob(16))))
    let active_int: i64 = if active { 1 } else { 0 };

    db.prepare(
        "INSERT INTO career (slug, title, description_md, department, type, active) \
         VALUES (?1, ?2, ?3, ?4, ?5, ?6)",
    )
    .bind(&[
        slug.clone().into(),
        title.into(),
        description_md.into(),
        department.into(),
        career_type.into(),
        active_int.into(),
    ])
    .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
    .run()
    .await
    .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?;

    // ── Re-query the inserted row ────────────────────────────────────────────
    let career = db
        .prepare(
            "SELECT id, slug, title, description_md, department, type, active, created_at \
             FROM career WHERE slug = ?1",
        )
        .bind(&[slug.into()])
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .first::<Career>(None)
        .await
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .ok_or_else(|| WorkerError::Internal("Career row missing after insert".into()))?;

    Response::from_json(&career)
        .map(|r| r.with_status(201))
        .map_err(|e| WorkerError::Internal(e.to_string()).into())
}

