/// Update an existing career listing by id.
///
/// Accepts a partial JSON body. Only fields present in the body are updated.
/// Validates `type` if provided.
/// Returns HTTP 200 with the updated resource.
///
/// Requires Admin role (enforced by router).
///
/// Requirements: 5.2, 5.6
pub async fn patch_career(
    mut req: Request,
    env: &Env,
    _ctx: SessionContext,
    id: &str,
) -> Result<Response> {
    let body: serde_json::Value = req
        .json()
        .await
        .unwrap_or(serde_json::Value::Object(serde_json::Map::new()));

    let db = env
        .d1("DB")
        .map_err(|e| WorkerError::Internal(e.to_string()))?;

    // ── Validate career_type if provided ─────────────────────────────────────
    if let Some(t) = body.get("type").and_then(|v| v.as_str()) {
        if !ALLOWED_CAREER_TYPES.contains(&t) {
            return error_to_response(WorkerError::Validation(
                ValidationError::InvalidInput(
                    "type must be one of: Full-Time, Part-Time, Contract, Internship".into(),
                ),
            ));
        }
    }

    // Build SET clauses dynamically — only include fields present in the body
    let mut set_clauses: Vec<String> = Vec::new();
    let mut bindings: Vec<wasm_bindgen::JsValue> = Vec::new();
    let mut param_idx: u32 = 1;

    macro_rules! push_field {
        ($json_key:expr, $col:expr, $extractor:expr) => {
            if let Some(val) = body.get($json_key).and_then($extractor) {
                set_clauses.push(format!("{} = ?{}", $col, param_idx));
                bindings.push(val.into());
                param_idx += 1;
            }
        };
    }

    push_field!("title",          "title",          |v| v.as_str().map(|s| s.to_string()));
    push_field!("description_md", "description_md", |v| v.as_str().map(|s| s.to_string()));
    push_field!("department",     "department",      |v| v.as_str().map(|s| s.to_string()));
    push_field!("type",           "type",            |v| v.as_str().map(|s| s.to_string()));
    push_field!("slug",           "slug",            |v| v.as_str().map(|s| s.to_string()));

    // active is a bool → stored as integer
    if let Some(active_bool) = body.get("active").and_then(|v| v.as_bool()) {
        let active_int: i64 = if active_bool { 1 } else { 0 };
        set_clauses.push(format!("active = ?{}", param_idx));
        bindings.push(active_int.into());
        param_idx += 1;
    }

    if set_clauses.is_empty() {
        return error_to_response(WorkerError::Validation(
            ValidationError::InvalidInput("No updatable fields provided".into()),
        ));
    }

    // Always bump updated_at for careers — the schema stores created_at only,
    // but we add updated_at if it exists; if not, we skip it gracefully.
    // (Schema only has created_at for career table per DDL.)

    let sql = format!(
        "UPDATE career SET {} WHERE id = ?{}",
        set_clauses.join(", "),
        param_idx
    );
    bindings.push(id.to_string().into());

    db.prepare(&sql)
        .bind(&bindings)
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .run()
        .await
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?;

    // ── Re-query updated row ─────────────────────────────────────────────────
    let career = db
        .prepare(
            "SELECT id, slug, title, description_md, department, type, active, created_at \
             FROM career WHERE id = ?1",
        )
        .bind(&[id.into()])
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .first::<Career>(None)
        .await
        .map_err(|e| WorkerError::Db(DbError::Query(e.to_string())))?
        .ok_or(WorkerError::NotFound)?;

    Response::from_json(&career)
        .map(|r| r.with_status(200))
        .map_err(|e| WorkerError::Internal(e.to_string()).into())
}

