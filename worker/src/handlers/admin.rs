// ---------------------------------------------------------------------------
// Applicant lifecycle transition helpers (Req 7.1)
// ---------------------------------------------------------------------------

/// Allowed `(current, next)` status transitions as specified in design
/// section 9.1 and requirement 7.1.
///
/// ```text
/// Applied       → Interviewing
/// Interviewing  → Offered
/// Interviewing  → Rejected
/// Offered       → Hired
/// Offered       → Rejected
/// ```
pub fn is_allowed_transition(current: ApplicationStatus, next: ApplicationStatus) -> bool {
    matches!(
        (current, next),
        (ApplicationStatus::Applied, ApplicationStatus::Interviewing)
            | (ApplicationStatus::Interviewing, ApplicationStatus::Offered)
            | (ApplicationStatus::Interviewing, ApplicationStatus::Rejected)
            | (ApplicationStatus::Offered, ApplicationStatus::Hired)
            | (ApplicationStatus::Offered, ApplicationStatus::Rejected)
    )
}

/// Convert an `ApplicationStatus` to its canonical string value as stored
/// in D1 (matches the `CHECK` constraint in the schema).
fn status_to_str(s: ApplicationStatus) -> &'static str {
    match s {
        ApplicationStatus::Applied => "Applied",
        ApplicationStatus::Interviewing => "Interviewing",
        ApplicationStatus::Offered => "Offered",
        ApplicationStatus::Hired => "Hired",
        ApplicationStatus::Rejected => "Rejected",
    }
}

